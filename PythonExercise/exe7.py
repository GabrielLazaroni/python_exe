# printando a media de uma nota de um aluno
# temos que somar as notas tiradas pelo aluno e dividir o resultado pelo numero de notas

nota = float(input('nota1:'))
nota2 = float(input('nota2:'))
nota_media = float((nota + nota2)/2)
print(f'nota1 foi {nota}, nota2 foi {nota2}, a soma das notas é {nota+nota2} e a média do aluno é {nota_media}')